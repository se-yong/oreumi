# None
print(type(None))

#Bool
print(type(True))
print(type(False))

#숫자형 int, float, complex(데이터과학, 머신러닝)
print(type(7))
print(type(3.7))
print(type(2 + 3j))

#문자형
print(type("아무거나"))

# 시퀀스(순서가있는) 자료형
print(type((1, 2, 4)))
print(type([1, 2, 4]))

# dict
print(type({"A":10, "B":20}))

# set
print(type({"1", "2", "4"}))

# function
def f():
    return 1
print(type(f))

